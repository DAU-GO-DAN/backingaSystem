    private void txtBranchManagerKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBranchManagerKeyPressed
        if(evt.getKeyChar()>='A' || evt.getKeyChar()>='a' || evt.getKeyChar()>='Z' || evt.getKeyChar()>='z' || evt.getKeyCode() == evt.VK_BACK_SPACE || evt.getKeyCode() == evt.VK_SPACE){
            txtBranchManager.setEditable(true);
        }else{
            txtBranchManager.setEditable(false);
        }
    }//GEN-LAST:event_txtBranchManagerKeyPressed

    
