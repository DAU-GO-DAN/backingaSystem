    private void bDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bDelActionPerformed
        int choice = JOptionPane.showConfirmDialog(null, "Bạn muốn xóa hết thông tin?");
        if(choice == JOptionPane.YES_OPTION){
            txtBranchAddress.setText("");
            txtBranchManager.setText("");
        }
    }//GEN-LAST:event_bDelActionPerformed

    private void bSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSaveActionPerformed
        int choice = JOptionPane.showConfirmDialog(null, "Bạn muốn lưu thông tin đã sửa?");
        if(choice == JOptionPane.YES_OPTION && bus.isUnique(lBranchIDInfor.getText())==false){
            BranchDTO braTemp = new BranchDTO();
            braTemp.setBranchAddress(bus.normalization(txtBranchAddress.getText()));
            braTemp.setBranchName(bus.generateBranchName(braTemp));
            braTemp.setBranchManager(bus.normalization(txtBranchManager.getText()));
            JOptionPane.showMessageDialog(null, "Lưu thành công");
            this.dispose();
            bus.edit(braTemp, lBranchIDInfor.getText());
        }else{
            JOptionPane.showMessageDialog(null, "BẠN BẤM NHẦM NÚT LƯU");
        }
    }//GEN-LAST:event_bSaveActionPerformed

    private void bAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAddActionPerformed
        int choice = JOptionPane.showConfirmDialog(null, "Bạn muốn thêm chi nhánh?");
        if(choice == JOptionPane.YES_OPTION && bus.isUnique(lBranchIDInfor.getText())){
            BranchDTO braTemp = new BranchDTO();
            braTemp.setBranchID(lBranchIDInfor.getText());
            braTemp.setBranchAddress(bus.normalization(txtBranchAddress.getText()));
            braTemp.setBranchName(bus.generateBranchName(braTemp));
            braTemp.setBranchManager(bus.normalization(txtBranchManager.getText()));
            JOptionPane.showMessageDialog(null, "Thêm thành công");
            this.dispose();
            bus.add(braTemp);
        }else{
            JOptionPane.showMessageDialog(null, "BẠN BẤM NHẦM NÚT THÊM");
        }
    }//GEN-LAST:event_bAddActionPerformed

