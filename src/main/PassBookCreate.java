    private void cbbMonthPeriodListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbbMonthPeriodListActionPerformed
        // TODO add your handling code here:
        interest = bookTypeBus.outputRateByMonth(cbbMonthPeriodList.getSelectedItem().toString());
        tfInterestRate.setText(interest+"");
        period = bookTypeBus.outputPeriodByMonth(cbbMonthPeriodList.getSelectedItem().toString());
        outAmount = (inAmount * Math.round(interest * period))/365 + inAmount;
        tfOutAmount.setText(doValid.formatMoney(outAmount));
    }//GEN-LAST:event_cbbMonthPeriodListActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        long number = Long.parseLong(JOptionPane.showInputDialog("Nhập số tiền: "));
        if(number > 0)
        {
            inAmount += number;
            tfAmount.setText("" +doValid.formatMoney(inAmount));
            period = bookTypeBus.outputPeriodByMonth(cbbMonthPeriodList.getSelectedItem().toString());
            outAmount = (inAmount * Math.round(interest * period))/365 + inAmount;
            tfOutAmount.setText(doValid.formatMoney(outAmount));
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnMinusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMinusActionPerformed
        // TODO add your handling code here:
        if (inAmount == 0) {
            JOptionPane.showMessageDialog(this, "Số Tiền Đã là 0!");
        }
        else{
            long number = Long.parseLong(JOptionPane.showInputDialog("Nhập số tiền: "));
            if(number > inAmount || number < 0)
            {
                JOptionPane.showMessageDialog(this, "Số Tiền trừ không hợp lệ!");
            }
            else{
                inAmount -= number;
                tfAmount.setText("" +doValid.formatMoney(inAmount));
                period = bookTypeBus.outputPeriodByMonth(cbbMonthPeriodList.getSelectedItem().toString());
                outAmount = (inAmount * Math.round(interest * period))/365 + inAmount;
                tfOutAmount.setText(doValid.formatMoney(outAmount));
            }
        }
    }//GEN-LAST:event_btnMinusActionPerformed

