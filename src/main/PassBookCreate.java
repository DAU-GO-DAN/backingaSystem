    public void loadAccList()
    {
        ArrayList<PaymentAccountDTO> accList = new ArrayList<>();
        ArrayList<PaymentAccountDTO> searchResult = accBus.searchByOwnerID(customer.getCustomerID());
        accList.addAll(searchResult);
        for(PaymentAccountDTO account : accList)
        {
            cbbAccList.addItem(account.getAccountNumber());
        }
    }
    
    void loadBranchList()
    {
        for(BranchDTO bra : braBus.braAL)
        {
            cbbBranchList.addItem(bra.getBranchName());
        }
    }
    
    void loadPeriod()
    {
        for(PassBookTypeDTO type : bookTypeBus.typeList)
        {
            cbbMonthPeriodList.addItem(type.getMonthPeriod());
        }
    }
    
    
    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        // TODO add your handling code here:
        PaymentAccountDTO account = new PaymentAccountDTO();
        account = accBus.getAccByNum(cbbAccList.getSelectedItem().toString());
        if(tfOutAmount.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Chưa nhập đủ thông tin");
        }
        else if(inAmount > account.getAccountBalance())
        {
            JOptionPane.showMessageDialog(this, "Số tiền ko đủ");
        }
        else{
            String bookTypeID = bookTypeBus.outputIDByMonth(cbbMonthPeriodList.getSelectedItem().toString());
            String cusID = customer.getCustomerID();
            String braID = braBus.outputIDByName(cbbBranchList.getSelectedItem().toString());
            LocalDate createdDate = LocalDate.now();
            LocalDate depositDate = createdDate.plusDays(period);
            PassBookDTO book = new PassBookDTO();
            book.setBookType(bookTypeID);
            book.setID(bookBus.generateID());
            book.setCustomerID(cusID);
            book.setInterestAmount(inAmount);
            book.setDepositAmount(outAmount);
            book.setCreatedDate(createdDate);
            book.setDepositDate(depositDate);
            book.setChangeDate(depositDate);
            book.setStatus("Trong kỳ hạn");
            book.setBranchID(braID);
            book.setMonth(cbbMonthPeriodList.getSelectedItem().toString());
            book.setInterest(interest);
            bookBus.add(book);
            account.setAccountBalance(account.getAccountBalance() - inAmount);
            accBus.edit(account.getAccountID(), account);
        }
        
    }//GEN-LAST:event_btnConfirmActionPerformed

