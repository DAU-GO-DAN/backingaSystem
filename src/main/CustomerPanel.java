    private void search(CustomerBUS bus)
    {
        String searchOpSelected = cbbSearchOption.getSelectedItem().toString();
        table.removeAll();
        bus.readData();
        ArrayList<CustomerDTO> searchList = new ArrayList<>();
        int count = 0;
        if(tfSearchBox.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập nội dung tìm kiếm");
        }

        //find by ID
        else if(searchOpSelected.equals("ID"))
        {
            searchList = bus.searchByID(tfSearchBox.getText());
        }

        //find by name
        else if(searchOpSelected.equals("Tên"))
        {
            searchList = bus.searchByName(tfSearchBox.getText());
        }

        //find by phone num
        else if(searchOpSelected.equals("Số Điện Thoại"))
        {
            searchList = bus.searchByPhone(tfSearchBox.getText());
        }

        //find by Email
        else if(searchOpSelected.equals("Email"))
        {
            searchList = bus.searchByEmail(tfSearchBox.getText());
        }

        //find by birth day
        else if(searchOpSelected.equals("Ngày Sinh"))
        {
            searchList = bus.searchByBirthDay(tfSearchBox.getText());
        }

        //find by month of birth day
        else if(searchOpSelected.equals("Tháng Sinh"))
        {
            searchList = bus.searchByMonthOB(Integer.parseInt(tfSearchBox.getText()));
        }

        //find by year of birth day
        else if(searchOpSelected.equals("Năm Sinh"))
        {
            searchList = bus.searchByYearOB(Integer.parseInt(tfSearchBox.getText()));
        }

        //find by created day
        else if(searchOpSelected.equals("Ngày Tạo"))
        {
            searchList = bus.searchByCreatedDay(tfSearchBox.getText());
        }
        
        count = searchList.size();
        if(searchList.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Không tìm thấy kết quả hợp lệ");
        }
        else{
            TableRefresh(searchList);
        }
        resultFound.setText("Tìm thấy "+count+" kết quả");
        
    }

    private void TableRefresh(ArrayList<CustomerDTO> customerList)
    {
        table.removeAll();
        table.revalidate();
        table.repaint();
        int count = customerList.size();
        
        if(count < 5)
        {
            table.setLayout(new GridLayout(5, 1));
        }

        else
        {
            table.setLayout(new GridLayout(count, 1));
        }

        for(CustomerDTO customer : customerList)
        {
            UserPanel userForm = new UserPanel(customer);
            table.add(userForm);
        }
        
    }
    
