    private int CusCount()
    {
        return bus.readDAO().size();
    }
    
    private int MaleCount()
    {
        int count = 0;
        for(CustomerDTO customer : bus.cusList)
        {
            if(customer.getGender().equals("Nam"))
            {
                count++;
            }
        }
        return count;
    }
    
    private int FemaleCount()
    {
        
        return CusCount() - MaleCount();
    }
    
    private int thisMonthJoin()
    {
        int count = 0;
        LocalDate currentDate = LocalDate.now();
        for(CustomerDTO customer : bus.cusList)
        {
            if(customer.getDayCreated().getMonthValue() == currentDate.getMonthValue())
            {
                count++;
            }
        }
        return count;
    }
    
    private int thisYearJoin()
    {
        int count = 0;
        LocalDate currentDate = LocalDate.now();
        for(CustomerDTO customer : bus.cusList)
        {
            if(customer.getDayCreated().getYear() == currentDate.getYear())
            {
                count++;
            }
        }
        return count;
    }

